# Ejercicio: Gestión de Inventario
# Descripción:
# Tu tarea es crear un programa para gestionar el inventario de una tienda. La tienda vende varios productos, y cada producto tiene un nombre, una cantidad en stock y un precio por unidad. Los datos de los productos se almacenarán en un diccionario donde la clave es el nombre del producto y el valor es otro diccionario con la cantidad y el precio.

# Requisitos:

# Crear una función para agregar un nuevo producto al inventario.
# Crear una función para actualizar la cantidad en stock de un producto existente.
# Crear una función para listar todos los productos en el inventario.
# Crear una función para calcular el valor total del inventario (cantidad en stock * precio por unidad).

# En principio realizarlo para que realice lo enunciado anteriormente para que funcione OKA. Luego en las mejoras separar en funciones y posibilidad de agregar menú y/o manejo de errores.


